    public boolean validation(){
        if(txtHead.getText().equals("")){
            JOptionPane.showMessageDialog(this,"please enter user name.");
            return false;
        }
       if(DateChosser.getDate() == null){
           JOptionPane.showMessageDialog(this, "please select a date.");
           return false;
       }
       if(txtAmountFirst.getText().equals("") || txtAmountFirst.getText().matches("[0-9]+") == false){
           JOptionPane.showMessageDialog(this,"please enter amount.");
           return false;
       }
       if(comboBoxPayment.getSelectedItem().toString().equalsIgnoreCase("Cheque")){
           if(txtChequeNo.getText().equals("")){
               JOptionPane.showMessageDialog(this,"please enter cheque number.");
               return false;
           }
           if(txtGSTNo.getText().equals("")){
               JOptionPane.showMessageDialog(this,"please enter bank name.");
               return false;
           }
       }
       if(comboBoxPayment.getSelectedItem().toString().equalsIgnoreCase("DD")){
           if(txtDDNo.getText().equals("")){
               JOptionPane.showMessageDialog(this, "please enter dd no.");
               return false;
           }
           if(txtBankName1.getText().equals("")){
               JOptionPane.showMessageDialog(this,"please enter bank name.");
               return false;
           }
       }
       if(comboBoxPayment.getSelectedItem().toString().equalsIgnoreCase("Card")){
           if(txtBankName1.getText().equals("")){
               JOptionPane.showMessageDialog(this,"please enter bank name");
               return false;
           }
       }
        return true;
    }
