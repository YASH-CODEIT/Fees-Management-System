    public int getReceiptNo(){
        int receipt_no=0;
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/fees_managment?zeroDateTimeBehavior=CONVERT_TO_NULL","root",""); 
            PreparedStatement pst=con.prepareStatement("select max(recieptNo) from fees_details");
            ResultSet rs=pst.executeQuery();
            if(rs.next() == true){
                receipt_no=rs.getInt(1);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
        return receipt_no+1;
    }
    public String insertData(){
        String status="";
        int recieptNo=Integer.parseInt(txtReceiptNo.getText());
        String studentName=txtHead.getText();
        String rollNo=txtRollNo.getText();
        String paymentMode=comboBoxPayment.getSelectedItem().toString();
        String chequeNo=txtChequeNo.getText();
        String bankName=txtBankName1.getText();
        String ddNo=txtDDNo.getText();
        String courseName=txtHead1.getText();
        String gstin=txtGSTNo.getText();
        float totalAmount=Float.parseFloat(txtSumTotal.getText());
        SimpleDateFormat dateFormat =new SimpleDateFormat("yyyy-MM-dd");
        String date=dateFormat.format(DateChosser.getDate());
        float initialAmount=Float.parseFloat(txtAmountFirst.getText());
        float cgst=Float.parseFloat(txtCgst.getText());
        float sgst=Float.parseFloat(txtSgst.getText());
        String totalInWords=txtTotalInWords.getText();
        String remark=textPaneRemark.getText();
        int year1=Integer.parseInt(txtFirstDate.getText());
        int year2=Integer.parseInt(txtLastDate.getText());
    try{
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/fees_managment?zeroDateTimeBehavior=CONVERT_TO_NULL","root","");  
        PreparedStatement pst=con.prepareStatement("insert into fees_details values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
        pst.setInt(1,recieptNo);
        pst.setString(2, studentName);
        pst.setString(3,rollNo);
        pst.setString(4,paymentMode);
        pst.setString(5,chequeNo);
        pst.setString(6, bankName);
        pst.setString(7,ddNo);
        pst.setString(8,courseName);
        pst.setString(9,gstin);
        pst.setFloat(10,totalAmount);
        pst.setString(11,date);
        pst.setFloat(12, initialAmount);
        pst.setFloat(13,cgst);
        pst.setFloat(14,sgst);
        pst.setString(15,totalInWords);
        pst.setString(16,remark);
        pst.setInt(17,year1);
        pst.setInt(18,year2);
        int rowCount=pst.executeUpdate();
        if(rowCount==1){
            status="Success";   
        }
        else{
            status="Failed";
        }
    }catch(Exception e){
        e.printStackTrace();
    }
    return status;
    }
